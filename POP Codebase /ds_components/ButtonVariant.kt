package com.pop.components.ds_components

/**
 * Button variants matching Figma design system
 * 
 * Both PopButtonV2 and PopIconButtonV2 support these variants
 */
enum class ButtonVariant {
    /**
     * Primary button variant - main action button
     */
    Primary,
    
    /**
     * Secondary button variant - secondary action button
     */
    Secondary,
    
    /**
     * Tertiary button variant - tertiary action button
     */
    Tertiary,
    
    /**
     * Brand Primary - brand-colored primary button (typically orange/accent)
     */
    BrandPrimary,
    
    /**
     * White button variant - white background button
     */
    White,
    
    /**
     * Ghost button variant - transparent background with border
     */
    Ghost,
    
    /**
     * Brand Ghost - ghost variant with brand color
     */
    BrandGhost
}

